// Declare a variable by using the 'var' keyword
var age;

// Assign a value to a variable by using equals
age = 12;

// It is also possible to declare and assign a variable at the same time
var name = "John";

// Use alerts to test that everything is behaving as expected
alert("My age is " + age);

// You can change the value of a variable by assigning a new value to it
// Note that we don't need to use the 'var' keyword to change the value of a variable.
// You only need to use the 'var' keyword when you are creating a NEW variable
age = 97;

alert("My age is now " + age);

// String concatenation is when we add multiple strings together to form a new string
alert(name + " is " + age + " years old");

